package by.epam.news.exception;

public class ActionLayerException extends Exception {

	private static final long serialVersionUID = 1L;

	public ActionLayerException() {
		super();
	}

	public ActionLayerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ActionLayerException(String arg0) {
		super(arg0);
	}

	public ActionLayerException(Throwable arg0) {
		super(arg0);
	}

}
